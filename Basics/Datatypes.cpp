#include<iostream>
using namespace std;

int main(){
    // integer data type
    int apples = 5;
    cout<<apples<<endl;
    cout<<"size of int: "<<sizeof(apples)<<endl;

    // character data type
    char sign = '$';
    cout<<sign<<endl;
    cout<<"Size of char: "<<sizeof(sign)<<endl;

    //bool data type
    bool flag = true;
    cout<<flag<<endl;
    cout<<"Size of bool: "<<sizeof(sign)<<endl;

    //float datatype
    float score = 3.14;
    cout<<score<<endl;
    cout<<"Size of float: "<<sizeof(score)<<endl;

    return 0;
}